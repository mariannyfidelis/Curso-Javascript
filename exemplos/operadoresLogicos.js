/*
    OPERADORES LÓGICOS

    && -> AND -> E
    || -> OR  -> OU
    !  -> NOT -> NEGAÇÃO

    - Permitem unir duas ou mais comparações 
    - Trabalham em conjunto com os operadores de comparação
*/
console.log("== OPERADOR LOGICO AND ==");
console.log(true && true);
console.log(true && false);
console.log(false && true);
console.log(false && false);

console.log("\n== OPERADOR LOGICO OR ==");
console.log(true || true);
console.log(true || false);
console.log(false || true);
console.log(false || false);